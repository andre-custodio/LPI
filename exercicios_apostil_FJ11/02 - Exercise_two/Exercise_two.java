//Imprima a soma de 1 até 1000.
public class Exercise_two {

    public static void main(String[] args) {
                   
        int sum = 0;
          
        for (int i=0; i <= 1000 ; i++) {
            sum = sum + i;
        }
          
        System.out.println("A soma de 1 a 1000 é: " + sum);
         
    }
}